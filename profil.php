        <p>Rumah Gemilang Indonesia (RGI), berdiri di lahan wakaf seluas 2.300 meter persegi yang bertempat di Kampung Kebon Kopi, Kelurahan Pengasinan, Kecamatan Sawangan, Kota Depok Jawa Barat. Rumah Gemilang Indonesia merupakan sebuah unit program pemberdayaan dan pusat pelatihan (<em>empowering and training center</em>) di bawah direktorat Program Lembaga Amil Zakat Nasional Alazhar. Secara resmi, RGI mulai beroperasi sejak 1 Juni 2009 dengan melakukan sosialisasi kepada masyarakat. Sebagai bagian dari program pemberdayaan Lembaga Amil Zakat Nasional Alazhar, RGI mengadopsi model pesantren yang fokus pada penyelenggaraan pendidikan non formal dalam kemasan <em>short course</em> (kursus singkat). Perpaduan ini bertujuan agar para peserta pelatihan RGI tidak hanya menyerap pengetahuan dan keterampilan unggul yang menjadi pondasi masa depan mereka, tapi juga memiliki pengetahuan akidah islam yang baik. Rumah Gemilang Indonesia saat ini telah membuka delapan jurusan yaitu:</p>
